def get_version():
    import re
    try:
        f = open(f"../MuRaL/_version.py")
    except EnvironmentError:
        return None
    for line in f.readlines():
        mo = re.match("__version__ = '([^']+)'", line)
        if mo:
            ver = mo.group(1)
            return 'v' + ver
    return None

